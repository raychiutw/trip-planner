// 台灣 country 前綴 anchor：Google 台灣地址格式為「[3碼郵遞區號]台灣縣市…」（新制 5-6 碼）。
// anchor 到開頭 + 限定台灣郵遞區號長度（恰 3 或 5-6 碼），避免誤判「台灣」當地名的他國地址：
// substring 會誤轉日本「東京都新宿区台灣広場」的 区；寬鬆 \d{0,5} 會讓「2台灣広場区」（街道號
// 充當郵遞區號）也誤判。台湾（簡體湾）一併納入 — 全簡體台灣地址的 country token 仍可轉
// （湾→灣 在 S2T_TW）。module-level 編譯一次。
const TW_PREFIX_RE = /^\s*(\d{3}|\d{5,6})?\s*(台灣|臺灣|台湾)/;

/**
 * 台灣地址簡轉繁。非台灣地址原樣回傳（保護日本等地址的簡日共用字形）。
 *
 * 台灣判定：開頭（可選郵遞區號）為「台灣」/「臺灣」country 前綴。只對台灣地址套用，
 * 避免誤動日本地址的「区/町/県/号」等簡日共用新字體。逐字查 S2T_TW，無對應者原樣保留。
 *
 * **失敗模式**：Google 改格式、或台灣地址無此前綴 → 靜默 no-op 回原字串（不報錯）。
 * 若簡體台灣地址重現，先查此前綴 gate 是否 match。
 */
export function toTraditionalTwAddress(s: string): string {
  if (!TW_PREFIX_RE.test(s)) return s;
  // for...of 逐字（正確處理 surrogate pair / astral plane；無對應字原樣保留）
  let out = '';
  for (const ch of s) out += S2T_TW[ch] ?? ch;
  return out;
}
