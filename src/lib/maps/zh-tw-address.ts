/**
 * 台灣地址簡轉繁 — Google Places address locale fix。
 *
 * Root cause（2026-06-27 取證）：Google Places API v1 對部分台灣 POI 的 address
 * （`formattedAddress` 與 `addressComponents` 兩者）回簡體中文，例如
 * 「929台灣屏东县琉球乡」。實測 `languageCode=zh-TW` / `zh-Hant-TW` / `regionCode=TW`
 * 皆無法改變（Google 端地址資料 locale，非請求參數問題）。`displayName`（店名）
 * 不受影響，仍為繁體。place_id ChIJHdrseQDlcTQRK06leVen9Uc（琉浪日嚐）為 prod case。
 *
 * 解法：對「判定為台灣的地址」做簡→繁逐字轉換。
 *
 * - **台灣判定**：地址開頭（可選台灣郵遞區號 3 或 5-6 碼）為「台灣」/「臺灣」/「台湾」
 *   country 前綴。Google 台灣地址格式為「[郵遞區號]台灣縣市…」，故 anchor 到開頭並限定
 *   郵遞區號長度。**不可**用 substring 判定 — 否則含「台灣」當地名的他國地址（如日本
 *   「東京都新宿区台灣広場」）會被誤判，其中的「区」等簡日共用新字體被誤轉成「區」
 *   （silent corruption，且 _poi / backfill 寫入路徑可達）。只對台灣地址套用，保護
 *   日本地址的「区/町/県/号」。
 *
 * - **僅收無破音歧義字**：簡體字形對應「唯一」繁體者才收。刻意排除一簡對多繁
 *   的歧義字（后→后/後、台→台/臺、干→干/乾/幹、松→松/鬆、发→發/髮、范→范/範；
 *   叶→葉/協、庄→莊/庄、机→機/几、宁→寧/贮、万→萬/万俟 等），避免「后里」誤轉
 *   「後里」、「松山」誤轉「鬆山」。這些歧義字若真為簡體需上下文判斷，超出單字
 *   mapping 能力。
 *
 * ponytail: 收錄台灣地址高頻無歧義簡繁字（行政區劃 + 常見地名/路名）。極罕見字
 *           漏網時可擴充 S2T_TW；需詞彙級完整簡轉繁可改 opencc-js（成本：bundle 字典）。
 */

